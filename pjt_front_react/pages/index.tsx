import { useState, useEffect } from "react";

import Head from "next/head";
import Router from "next/router";

import LargeText from "../common/LargeText";
import MainButton from "../common/MainButton";
import IndexAnimation from "../component/IndexAnimation";
import styled from "styled-components";

export default function Home() {
  const route = Router;

  let vh = 0;

  useEffect(() => {
    vh = window.innerHeight * 0.01;
    document.documentElement.style.setProperty("--vh", `${vh}px`);
  }, []);

  const moveToLogin = () => {
    route.push("/login");
  };

  const moveToSignup = () => {
    route.push("/signup");
  };

  return (
    <>
      <Head>
        <meta name='description' content='Generated by create next app' />
        <meta name='viewport' content='width=device-width, initial-scale=1' />
        <link rel='icon' href='/favicon.ico' />
      </Head>
      <main>
        <IndexBox>
          <TitleBox>
            <IndexAnimation />
            <LargeText> SSEEKER</LargeText>
          </TitleBox>
          <ButtonBox>
            <MainButton onButtonClick={moveToLogin}> LogIn</MainButton>
            <MainButton onButtonClick={moveToSignup}> SignUp</MainButton>
          </ButtonBox>
        </IndexBox>
      </main>
    </>
  );
}

const IndexBox = styled.div`
  display: flex;
  flex-direction: column;
  height: 100vh;
  height: calc(var(--vh, 1vh) * 100);
  
`;

const TitleBox = styled.div`
  display: flex;
  flex-direction: column;
  align-items: center;
  margin-top: auto;
`;

const ButtonBox = styled.div`
  display: flex;
  flex-direction: column;
  margin: 0px 15px;
  margin-top: auto;
`;
